export class Sphere {
  /**
   *
   * @param {vector3} center
   * @param {float} radius
   * @param {vector3} color
   * @param {float} shine
   */
  constructor(center, radius, color, shine) {
    this.center = center;
    this.radius = radius;
    this.color = color;
    this.shine = shine;
  }
}
